const fs = require("node:fs");
const path = require("node:path");
const { spawnSync } = require("node:child_process");

const rootDir = path.resolve(__dirname, "..");
const testsDir = path.join(rootDir, "server", "tests");
const coverageDir = path.join(rootDir, "coverage");
const tapDir = path.join(coverageDir, "tap");

function escapeMarkdown(value) {
  return String(value ?? "")
    .replace(/\\/g, "\\\\")
    .replace(/\|/g, "\\|")
    .replace(/\r?\n/g, " ");
}

function parseTap(tap, file) {
  const records = [];
  let current = null;

  tap.split(/\r?\n/).forEach((line) => {
    const result = line.match(/^(\s*)(ok|not ok)\s+\d+\s+-\s+(.+?)(?:\s+#\s*(SKIP|TODO)\b.*)?$/);

    if (result) {
      current = {
        file,
        indent: result[1].length,
        ok: result[2] === "ok",
        title: result[3].trim(),
        directive: result[4] || "",
        durationMs: null,
        type: "test"
      };
      records.push(current);
      return;
    }

    if (!current) {
      return;
    }

    const duration = line.match(/^\s*duration_ms:\s*([0-9.]+)/);
    if (duration) {
      current.durationMs = Number(duration[1]);
      return;
    }

    if (/^\s*type:\s*'suite'/.test(line)) {
      current.type = "suite";
    }
  });

  return records
    .filter((record) => record.type !== "suite")
    .map((record) => {
      let status = record.ok ? "passed" : "failed";

      if (record.directive === "SKIP") {
        status = "skipped";
      }

      if (record.directive === "TODO") {
        status = "todo";
      }

      return {
        file: record.file,
        title: record.title,
        status,
        durationMs: record.durationMs
      };
    });
}

function summarize(files, tests, startedAt, finishedAt) {
  const count = (status) => tests.filter((test) => test.status === status).length;

  return {
    startedAt,
    finishedAt,
    durationMs: new Date(finishedAt).getTime() - new Date(startedAt).getTime(),
    files: files.length,
    tests: tests.length,
    passed: count("passed"),
    failed: count("failed"),
    skipped: count("skipped"),
    todo: count("todo")
  };
}

function buildMarkdown(report) {
  const summaryRows = [
    ["Files", report.summary.files],
    ["Tests", report.summary.tests],
    ["Passed", report.summary.passed],
    ["Failed", report.summary.failed],
    ["Skipped", report.summary.skipped],
    ["Todo", report.summary.todo],
    ["Duration", `${report.summary.durationMs} ms`]
  ]
    .map(([label, value]) => `| ${label} | ${value} |`)
    .join("\n");

  const testRows = report.tests
    .map(
      (test) =>
        `| \`${escapeMarkdown(test.file)}\` | ${escapeMarkdown(test.title)} | ${test.status} | ${
          test.durationMs === null ? "" : test.durationMs.toFixed(2)
        } |`
    )
    .join("\n");

  return `# Test Results

Generated by \`npm run test:results\` at ${report.summary.finishedAt}.

## Summary

| Metric | Value |
| --- | ---: |
${summaryRows}

## All Tests

| File | Test | Status | Duration, ms |
| --- | --- | --- | ---: |
${testRows}
`;
}

function main() {
  fs.mkdirSync(tapDir, { recursive: true });

  const testFiles = fs
    .readdirSync(testsDir)
    .filter((filename) => filename.endsWith(".test.js"))
    .sort();
  const startedAt = new Date().toISOString();
  const tests = [];
  let exitCode = 0;

  testFiles.forEach((file) => {
    const absoluteFile = path.join(testsDir, file);
    const tapFile = path.join(tapDir, `${file}.tap`);

    console.log(`\n[Test file] ${file}`);

    const result = spawnSync(
      process.execPath,
      [
        "--test",
        "--test-concurrency=1",
        "--test-reporter=spec",
        "--test-reporter-destination=stdout",
        "--test-reporter=tap",
        `--test-reporter-destination=${tapFile}`,
        absoluteFile
      ],
      {
        cwd: rootDir,
        encoding: "utf8",
        stdio: ["ignore", "inherit", "inherit"]
      }
    );

    if (result.status !== 0) {
      exitCode = result.status || 1;
    }

    if (fs.existsSync(tapFile)) {
      tests.push(...parseTap(fs.readFileSync(tapFile, "utf8"), file));
    }
  });

  const finishedAt = new Date().toISOString();
  const report = {
    summary: summarize(testFiles, tests, startedAt, finishedAt),
    tests
  };

  fs.writeFileSync(path.join(coverageDir, "test-results.json"), JSON.stringify(report, null, 2));
  fs.writeFileSync(path.join(coverageDir, "test-results.md"), buildMarkdown(report));

  console.log(
    `\ntest-results-table: ${report.summary.passed}/${report.summary.tests} passed, ${report.summary.failed} failed`
  );
  console.log("test-results-files: coverage/test-results.md, coverage/test-results.json");

  process.exitCode = exitCode;
}

main();
